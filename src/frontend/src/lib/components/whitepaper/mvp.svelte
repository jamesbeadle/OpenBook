<h1 class="text-2xl">Minimum Viable Product (MVP)</h1>
<div class="flex flex-col space-y-4 mt-4">
  <p>
    OpenBook will conduct it's decentralisation sale as a fully integrated
    platform, encompassing both sales and accountancy functions.
  </p>
  <p>
    With a focus on transactional-based systems, OpenBook is engineered for the
    accurate reconciliation of multi-currency finance data, catering to specific
    jurisdictional needs. Key design components of OpenBook’s MVP include:
  </p>

  <h1 class="text-2xl">Multi-Role</h1>
  <p>
    OpenBook is designed to support various user roles, such as finance
    professionals and sales representatives. Each role within the platform will
    have access to tailored tools and features, enhancing efficiency and user
    experience. This multi-role approach ensures that every user, regardless of
    their function, finds the platform adaptable to their specific needs.
  </p>
  <h1 class="text-2xl">Multi-Tenant</h1>
  <p>
    Recognising the complex needs of modern business professionals, OpenBook
    will enable users to manage and navigate across multiple organisations
    effortlessly. This is especially beneficial for users managing or
    interacting with different businesses or client accounts, providing a
    seamless and integrated experience.
  </p>
  <h1 class="text-2xl">Multi-Currency</h1>
  <p>
    OpenBook will support transactions in both fiat and cryptocurrencies. This
    feature is vital for businesses engaged in international operations or those
    operating in the crypto space, offering a cohesive view of financial
    activities across different monetary systems.
  </p>
  <p>
    These foundational features of OpenBook’s MVP are set to provide a
    versatile, flexible, and comprehensive toolset, meeting the diverse
    requirements of businesses in today's dynamic environment.
  </p>
